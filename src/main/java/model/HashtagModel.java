package model;

public class HashtagModel
{

	private int[] indices;
	private String text;


	public int[] getIndices()
	{
		return this.indices;
	}

	public void setIndices(int[] indices)
	{
		this.indices = indices;
	}

	public String getText()
	{
		return this.text;
	}

	public void setText(String text)
	{
		this.text = text;
	}
}